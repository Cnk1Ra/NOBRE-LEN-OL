// Get translation with fallback
function getEmailText(lang: string, key: string): string {
  const langTexts = emailTranslations[lang] || emailTranslations['pt-BR']
  return langTexts[key] || emailTranslations['pt-BR'][key] || key
}

// Detect language from code (supports partial matches)
function detectLanguage(langCode?: string): string {
  if (!langCode) return 'pt-BR'

  // Direct match
  if (emailTranslations[langCode]) return langCode

  // Try base language (e.g., 'en' from 'en-AU')
  const baseCode = langCode.split('-')[0]
  const matchingLang = Object.keys(emailTranslations).find(l => l.startsWith(baseCode))

  return matchingLang || 'pt-BR'
}

